<font class="content"><p align="justify"><br/>
Mar Saba as an Important Bird Area For the Lesser Kestrel- Falco Naumannni<br/>
<br/>
    Palestine is part of the Great Rift Valley, which lies in the 
south-western part of it. An area located at the junction of three continents 
Europe, Asia, and Africa, to the eastern shore of the Mediterranean. Just to the 
north of the equator between two-latitude 30` 30 and 15` 32 north, and between 
two-longitude 15` 34 and 40` 35 north, Palestine location together with diverse 
topographical features creates different climatic system that give it a fact of 
being one of the richest countries in the Middle East for birds. Therefore, 
several areas had a special importance as stop over sites, while others lie 
under major routes for migratory and passage bird species. In addition to many 
site that meet with Birdlife International and IUCN for Important Bird areas.
<br/>
Upon this fact about 13 sites both in the West Bank and Gaza Strip were 
recognized to be Important Bird Areas in Palestine on a national level up to 
