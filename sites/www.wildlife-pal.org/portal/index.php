Dear Friends and Supporters:,<br/>
<br/>
It is our pleasure to introduce you to Palestine Wildlife Society. <br/>
<br/>
    Palestine Wildlife Society is an active, professional, 
Non-Profit Organization whose scope of work covers the West Bank and Gaza with a 
multidimensional mission aiming at the conservation and enhancement of the 
biodiversity and wildlife in Palestine.<br/>
<br/>
    Palestine is known for its unique combination of resources 
and species of birds, plants and mammals; all of which make Palestine the ideal 
region to study for biologists, researchers, archeologists and many others. 
Despite Palestine&rsquo;s significance, there have been minimal efforts to protect and 
conserve the natural areas and wildlife. Many of the natural habitats are 
rapidly being polluted and destroyed, which is resulting in the endangerment of 
numerous species of plants, birds, and mammals, and which ultimately negatively 
effects the human population. Thus, in response to urgent need to conserve the 
region, Palestine Wildlife Society (PWLS) was established in 1999 in Beit Sahour 
(Bethlehem District) just 10 Km south of Jerusalem..<br/>
<br/>
    From its inception, PWLS has been a full partner with 
Birdlife International in regards to the IBA's Chapter and has pursued clear 
objectives regarding environmental education and the conservation of the nature 
and biodiversity. Over the years, PWLS has demonstrated its dedication and 
effectiveness in raising environmental awareness throughout the region. <br/>
<br/>
PWLS strives to fulfill its mission through its programs and activities. Some of 
our programs include: <br/>
<br/>
    Establishing two Wildlife Monitoring and Ringing/banding 
Stations, training local &amp; regional Ecologists on Nature Conservation and 
Management, Environmental Education of school children, educating tour guides on 
Eco-Tourism, and training teachers, farmers, heads of households, women and 
students to be proactive leaders their community concerning environmental 
issues. In addition to these programs, PWLS conducts research on environmental 
issues &amp; works on developing new methods of conservation. PWLS also produces 
documentaries, brochures and other publications in order to facilitate the 
environmental education of both the members of local and international 
communities.<br/>
<br/>
    At present, one of PWLS&rsquo; main focuses is Conservation 
Education. PWLS believes that Conservation Education is the main pillar in the 
conservation movement of any region. Therefore we have worked intensively in the 
field of awareness and education. As a result of our efforts, the subject of 
conservation of the environment was added to the national education curriculum 
of the Ministry of Education in Palestine and was part of the National 
Biodiversity Strategy and Action Plan for Palestine with Ministry of 
Environmental Affairs. <br/>
<br/>
    Should you wish to cooperate with Palestine Wildlife Society, 
we shall be pleased work with you on the behalf of conservation of the 
environment and wildlife of the region. We would also greatly appreciate any 
financial support to maintain existing programs, develop new projects or conduct 
further research activities.<br/>
<br/>
    Finally, we would like to thank you for your interest and to 
express our deep appreciation for your cooperation with Palestine Wildlife 
Society. <br/>
